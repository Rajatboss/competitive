
public class String_mutation {

	public static void main(String[] args) {
			System.out.println("Rajat".length());
			System.out.println("Rajat".concat(" Piyush"));
			System.out.println("Rajat".equals("Rajat"));
			System.out.println("Rajat".substring(0, 3));
			System.out.println("     rajat     ".trim());

	}

}
